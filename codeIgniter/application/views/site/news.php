						<h2>Un robot aspirateur chantant</h2>
						<p>La société japonaise SHARP, spécialisée dans l’électronique, a créé (en collaboration avec Yamaha) COCOROBO, un aspirateur chantant sur commande.</p>
						<p>Si vous lui dite des phrase (en japonais) comme:</p>
						<ul>
							<li>"Je veux sourire"</li>
							<li>"Je veux du son"</li>
							<li>"Je veux me relaxer"</li>
						</ul>
						<p>COCOROBO pourra chanter 5 chansons :</p>
						<ul>
							<li><a href="http://www.nicovideo.jp/watch/sm29776469" target="_blank">"Lovely Ensemble"</a></li>
							<li><a href="http://www.nicovideo.jp/watch/sm29765704" target="_blank">"Hanauta Biyori – Souji Sentaku Kaji Watashi"</a></li>
							<li><a href="http://www.nicovideo.jp/watch/sm29796036" target="_blank">"Cocorovoice"</a></li>
							<li><a href="http://www.nicovideo.jp/watch/sm29772866" target="_blank">"ALL*DAY*LUV"</a></li>
							<li><a href="http://www.nicovideo.jp/watch/sm29764821" target="_blank">"ASHIOTO"</a></li>
						</ul>
						<p>De plus COCOROBO apprendra et évoluera au fil du temps en fonction de vos commentaires et de vos préférences. Il sera même possible de faire vos propre chansons en passent par le logiciel VOCALOID.</p>
